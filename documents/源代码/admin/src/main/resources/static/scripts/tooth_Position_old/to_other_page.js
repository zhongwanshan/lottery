//订单页面切换（今日订单,所有订单,不合格订单）
function change_pages() {
    $("#today_case").click(function () {
        window.location.href = "today"
    })
    $("#all_case").click(function () {
        window.location.href = "all"
    })

    $("#unqualified_list").click(function () {
        window.location.href = "unqualified"
    })
}

jQuery(document).ready(function () {
    change_pages();

})