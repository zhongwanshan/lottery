function getLangDict(dictName, key) {
    // 多语言支持
    var dict = {};

    var un32_vars = un32_vars || {};
    var lang = "";
    if (undefined == un32_vars || un32_vars == null) {
        lang = 'zh';
    } else {
        lang = un32_vars['lang'];
    }

    if (lang == 'en')
        dict = Dicts_en;
    else if (lang == 'ja')
        dict = Dicts_ja;
    else if (lang == 'ko')
        dict = Dicts_ko;
    else
        dict = Dicts;

    // 多语言支持
    var value = "";
    if (dict[dictName] != undefined && dict[dictName][key] != undefined) {
        value = dict[dictName][key];
    }

    return value;
}
